import Foundation
import UIKit

extension UIViewController{
    
    func getScreenWidth() -> CGFloat {
        return UIScreen.main.bounds.size.width
    }
    
    func getContainerHeight() -> CGFloat {
        return UIScreen.main.bounds.size.height / 5 * 2
    }
    
}

